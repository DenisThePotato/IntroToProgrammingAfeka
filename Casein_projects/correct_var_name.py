def correct_var_name(name):
    """
    in each of the following conditions
    if variable name is incorrect suggest a corrected one after the "return".
    is the name is a valid variable name return the original one.
    """
    if name == "4u":
        return "forYou"
    elif name == "#TooCool":
        return "tooCool"
    elif name == "value":
        return name
    elif name == "specially4u":
        return name
    elif name == "is":
        return "its"
    elif name == "smart one":
        return "smartOne"